clear; clc; close all;

% 1. ΠΑΡΑΜΕΤΡΟΙ ΠΡΑΓΜΑΤΙΚΟΥ ΣΥΣΤΗΜΑΤΟΣ
g = 9.81;        % Επιτάχυνση βαρύτητας
l_true = 1.5;    % Πραγματικό μήκος l (επιλογή [1, 2.5])
c_true = 0.5;    % Πραγματική απόσβεση c (επιλογή [0.2, 0.8])

% Οι πραγματικές "άγνωστες" παράμετροι (όπως τις ορίσαμε στη θεωρία)
theta1_star = g / l_true; 
theta2_star = c_true;

% =========================================================================
% 2. ΠΑΡΑΜΕΤΡΟΙ ΕΚΤΙΜΗΤΗ & LYAPUNOV
% =========================================================================
gamma1 = 15;     % Κέρδος προσαρμογής για το theta1 (μήκος)
gamma2 = 5;      % Κέρδος προσαρμογής για το theta2 (απόσβεση)
am = 10;         % Κέρδος Series-Parallel (για το σφάλμα ταχύτητας)
am1 = 10;        % Κέρδος διόρθωσης για το σφάλμα θέσης (προαιρετικό για το θ_hat)

tspan = [0 100]; % Χρόνος προσομοίωσης

% Αρχικές συνθήκες: [theta, theta_dot, theta_hat, theta_dot_hat, theta1_hat, theta2_hat]
% Έστω ότι ξεκινάμε με λάθος εκτίμηση l=1.0 και c=0.2
X0 = [0; 0; 0; 0; g/1.0; 0.2]; 

% =========================================================================
% 3. ΠΡΟΣΟΜΟΙΩΣΗ (ΧΩΡΙΣ ΘΟΡΥΒΟ - ΘΕΜΑ 2α)
% =========================================================================
noise_amp = 0; % Μηδενικός θόρυβος για το 2α
[t, X] = ode45(@(t,X) pendulum_lyapunov(t, X, theta1_star, theta2_star, gamma1, gamma2, am, am1, noise_amp), tspan, X0);

% Εξαγωγή δεδομένων
theta     = X(:,1);
theta_dot = X(:,2);
theta_hat = X(:,3);
theta_dot_hat = X(:,4);
th1_hat   = X(:,5);
th2_hat   = X(:,6);

% Υπολογισμός πραγματικών μεγεθών (Μετατροπή theta1_hat πίσω σε l_hat)
l_hat = 9.81 ./ th1_hat; 

% Υπολογισμός σφαλμάτων
e_theta = theta - theta_hat;
e_l = l_hat - l_true;
e_c = th2_hat - c_true;

% =========================================================================
% 4. ΓΡΑΦΙΚΕΣ ΠΑΡΑΣΤΑΣΕΙΣ
% =========================================================================
figure('Name', 'Εκτίμηση Παραμέτρων (Μέθοδος Lyapunov)', 'Position', [100, 100, 800, 800]);

subplot(4,1,1);
plot(t, theta, 'b', 'LineWidth', 1.5); hold on;
plot(t, theta_hat, 'r--', 'LineWidth', 1.5);
title('Γωνία \theta(t) vs Εκτιμώμενη \theta_{hat}(t)');
legend('\theta', '\theta_{hat}'); grid on;

subplot(4,1,2);
plot(t, e_theta, 'g', 'LineWidth', 1.5);
title('Σφάλμα Γωνίας e_\theta(t)'); grid on;

subplot(4,1,3);
plot(t, e_l, 'k', 'LineWidth', 1.5);
title('Σφάλμα Εκτίμησης Μήκους e_l(t) (Πρέπει να μηδενιστεί)'); grid on;

subplot(4,1,4);
plot(t, e_c, 'm', 'LineWidth', 1.5);
title('Σφάλμα Εκτίμησης Απόσβεσης e_c(t) (Πρέπει να μηδενιστεί)'); 
xlabel('Χρόνος (sec)'); grid on;


% =========================================================================
% ΣΥΝΑΡΤΗΣΗ ΔΥΝΑΜΙΚΟΥ ΣΥΣΤΗΜΑΤΟΣ (ΟΠΩΣ ΣΤΗ ΜΑΘΗΜΑΤΙΚΗ ΑΠΟΔΕΙΞΗ)
% =========================================================================
function dX = pendulum_lyapunov(t, X, th1_star, th2_star, gamma1, gamma2, am, am1, noise_amp)
    
    % Καταστάσεις
    x1 = X(1);         % Πραγματικό theta
    x2 = X(2);         % Πραγματικό theta_dot
    x1_hat = X(3);     % Εκτιμώμενο theta
    x2_hat = X(4);     % Εκτιμώμενο theta_dot
    th1_hat = X(5);    % Εκτίμηση theta1 (g/l)
    th2_hat = X(6);    % Εκτίμηση theta2 (c)
    
    % Είσοδος συστήματος (Συνθήκη Επιμένουσας Διέγερσης)
    u = 0.5 * sin(t);
    
    % Προσθήκη θορύβου στις μετρήσεις (για το Θέμα 2β, εδώ είναι 0)
    eta = noise_amp * sin(20 * pi * t);
    x1_m = x1 + eta;
    x2_m = x2 + eta;
    u_m  = u + eta;
    
    % Μη-Γραμμικές Συναρτήσεις (όπως στην απόδειξη)
    % Προσοχή: Ο εκτιμητής τροφοδοτείται με τις *μετρήσεις* (Series-Parallel)
    f1 = -sin(x1_m); 
    f2 = -x2_m;
    
    % --- ΠΡΑΓΜΑΤΙΚΟ ΣΥΣΤΗΜΑ ---
    dx1 = x2;
    dx2 = th1_star * f1 + th2_star * f2 + u; 
    
    % --- ΣΦΑΛΜΑ ΠΑΡΑΚΟΛΟΥΘΗΣΗΣ ΤΑΧΥΤΗΤΑΣ ---
    e = x2_m - x2_hat;
    
    % --- ΜΟΝΤΕΛΟ ΕΚΤΙΜΗΣΗΣ (Series-ParalleSl) ---
    % dx1_hat = x2_hat + L_gain * error_position (κλασικός παρατηρητής θέσης)
    dx1_hat = x2_hat + am1 * (x1_m - x1_hat); 
    
    % Εδώ είναι η εξίσωση που αποδείξαμε!
    dx2_hat = th1_hat * f1 + th2_hat * f2 + u_m + am * e;
    
    % --- ΝΟΜΟΙ ΠΡΟΣΑΡΜΟΓΗΣ (Από τη Lyapunov) ---
    % Ακριβώς αυτοί που βγάλαμε για να ακυρωθούν τα \tilde{theta}
    dth1_hat = gamma1 * e * f1;
    dth2_hat = gamma2 * e * f2;
    
    % Επιστροφή παραγώγων
    dX = [dx1; dx2; dx1_hat; dx2_hat; dth1_hat; dth2_hat];
end